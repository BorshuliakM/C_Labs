#include <stdio.h>

int main() {
    int N, M;
    printf("Введіть кількість маршрутів (N) і кількість зупинок (M): ");
    scanf("%d %d", &N, &M);

    if (N < 1 || M < 1) {
        printf("Кількість маршрутів і зупинок повинна бути більшою за 0.\n");
        return 1;
    }

    int routes[N][M]; // Оголошення таблиці розкладу руху
    int stopCount[M]; // Масив для підрахунку кількості автобусів на кожній зупинці

    // Ввід значень таблиці розкладу руху
    printf("Введіть таблицю розкладу руху %d маршрутів по %d зупинокам:\n", N, M);
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < M; j++) {
            scanf("%d", &routes[i][j]);
        }
    }

    // Ініціалізація масиву кількості автобусів на кожній зупинці
    for (int j = 0; j < M; j++) {
        stopCount[j] = 0;
    }

    // Підрахунок кількості автобусів на кожній зупинці
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < M; j++) {
            if (routes[i][j] != 0) {
                stopCount[j]++;
            }
        }
    }

    // Знаходження максимальної кількості автобусів на зупинці
    int maxCount = 0;
    for (int j = 0; j < M; j++) {
        if (stopCount[j] > maxCount) {
            maxCount = stopCount[j];
        }
    }

    // Виведення зупинок з максимальною кількістю автобусів
    printf("Зупинки з максимальною кількістю автобусів:\n");
    for (int j = 0; j < M; j++) {
        if (stopCount[j] == maxCount) {
            printf("Зупинка %d\n", j + 1);
        }
    }

    return 0;
}