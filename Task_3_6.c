#include <stdio.h>

int main() {
    int p, n;
    printf("Введіть кількість рядків (p) і стовпців (n) таблиці В: ");
    scanf("%d %d", &p, &n);

    if (p < 1 || n < 1) {
        printf("Розмір таблиці повинен бути більшим за 0.\n");
        return 1;
    }

    double B[p][n]; // Оголошення таблиці В
    double C[n];    // Оголошення масиву C

    // Ввід значень таблиці В
    printf("Введіть значення таблиці В:\n");
    for (int i = 0; i < p; i++) {
        for (int j = 0; j < n; j++) {
            scanf("%lf", &B[i][j]);
        }
    }

    // Обчислення елементів масиву Cj
    for (int j = 0; j < n; j++) {
        C[j] = 0.0; // Ініціалізуємо суму для кожного стовпця

        for (int i = 0; i < p; i++) {
            C[j] += B[i][j];
        }
    }

    // Виведення елементів масиву Cj
    printf("Елементи масиву Cj (суми стовпців таблиці В):\n");
    for (int j = 0; j < n; j++) {
        printf("C%d: %lf\n", j + 1, C[j]);
    }

    return 0;
}