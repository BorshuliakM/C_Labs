#include <stdio.h>

int main() {
    int n;
    printf("Введіть натуральне число n (> 1): ");
    scanf("%d", &n);

    // Перевірка на коректність n
    if (n <= 1) {
        printf("n повинно бути більше 1.\n");
        return 1;
    }

    // Ініціалізація перших двох членів послідовності
    int u0 = 0, u1 = 1;
    printf("Перші %d членів послідовності Фібоначчі:\n", n);
    printf("%d %d ", u0, u1);

    // Обчислення та виведення решти членів послідовності
    for (int i = 2; i < n; i++) {
        int ui = u0 + u1;
        printf("%d ", ui);
        u0 = u1;
        u1 = ui;
    }

    printf("\n");
    return 0;
}



